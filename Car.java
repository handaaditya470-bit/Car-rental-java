public class Car {

    int id;
    String name;
    String year;
    int pricePerDay;
    boolean available;

    public Car(int id, String name, String year, int pricePerDay){

        this.id = id;
        this.name = name;
        this.year = year;
        this.pricePerDay = pricePerDay;
        this.available = true;

    }

}